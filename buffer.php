<div class="post"><div class="posttop"><div class="username">Planetwatcher</div><div class="date">2005-May-22, 08:14 PM</div></div><div class="posttext">The ISS is far from being a dead end program.  We have and are learning a lot from having it.  We are learning how humans act and react spending long amounts of time in space.  We are beginning to learn how to counter such problems.<br />
<br />
We are or soon will be learning how to grow plants and eventually crops in low or no gravity environment.<br />
We are finding out how durable our present level of technology is and how well it will protect us from that which we need protecting from.<br />
<br />
It is even likely that the ISS will help us to learn about new hazards, and problems we either never thought of, or never encountered before.<br />
<br />
Even the dismal failures we have and are expierencing with ISS are teaching us things that we otherwise wouldn&amp;#39;t haave learn until we were underway to some distant sentenal in space and run into problematic situations months, years, or even decades away from Earth and any potentual help.<br />
This will save an untold number of lives in the future.<br />
<br />
The space shuttle likewise is not a failure, even though people have died, which is no less then tragic.<br />
But what if we had not attempted to build a reusable space craft until we had the means to get to the middle or outer solar system?  <br />
Then had a fleet of them roaming the solar system; which acrued the problems that killed the other two crews?<br />
<br />
We could have ended up with a lot more people dying because of larger crews and more ships, and when it happened,  we would have been unable to get them back safely before they were killed.<br />
<br />
If anything we need to increase our presence in space, and learn how to decrease the amount of time it takes to get new innovations from the drawing board to the launch pad.<br />
That lag time is so long that the technology is obsolete by the time we build it.<br />
<br />
The shuttle and ISS are case in point.<br />
While the conceipt of a space shuttle is revolutionary, the technology for it was engineered in the 1970s.  And mostly the early 1970s at that.<br />
The ISS had been on the boards since the early 1980s.  But assembly in space didn&amp;#39;t begin until the late 1990s.<br />
Unlike the Saturn V rocket which was conceived in the mid 1960s, but in space by 1968.  Or Skylab which was thought of in 1970, and launched in may 1973.<br />
<br />
The Pluto/Keipler belt mission has been talked about and planned since at least 1998, but won&amp;#39;t launch much before 2010.<br />
Missions to Mars planned for 20 or more years in the future will use todays technology.<br />
<br />
Clearly we are heading the wrong direction.  It is taking longer to get ideas from the drawing board to space, when it should be getting shorter.</div></div><hr />

 
